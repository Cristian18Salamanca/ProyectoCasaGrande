package Modelo;

public class codigo {
    
    int id;
    String cod;
    
    public codigo() {}

    public codigo(int id, String cod) {
        this.id = id;
        this.cod = cod;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCod() {
        return cod;
    }

    public void setCod(String cod) {
        this.cod = cod;
    }
    
}
